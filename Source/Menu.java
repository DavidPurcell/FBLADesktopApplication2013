    public static void fileCreate() throws IOException
    {
        //Create files and folders if the do not exist
        File employees = new File("CSVFiles\\Employees.txt");
        File employers = new File("CSVFiles\\Employers.txt");
        File evaluations = new File("CSVFiles\\Evaluations.txt");
        File fieldplacements = new File("CSVFiles\\FPs.txt");
        File EvaluationReports = new File("EvaluationReports");
        File EmployeeReports = new File("EvaluationReports\\EmployeeReports");
        String files = "";
        File file = new File("CSVFiles");
        if(!EvaluationReports.exists())
        {
            EvaluationReports.mkdirs();
            files += "Directory: EvaluationReports\\EmployeeReports \n";
        }
        if(!EmployeeReports.exists())
        {
            EmployeeReports.mkdirs();
            files += "Directory: EvaluationReports \n";
        }
        if(!file.exists())
        {
            file.mkdirs();
            files += "Directory: CSVFiles \n";
        }
        if(!employees.exists())
        {
            BufferedWriter bw = new BufferedWriter(new FileWriter(employees));
            bw.write("EmployeeID, First Nameawer, Last Name, e-mail Address, Phone Number, Cell Number, Street Address, City, State, Zip Code,");
            bw.close();
            files += "CSVFiles\\Employees.txt \n";
        }
        if(!employers.exists())
        {
            BufferedWriter bw = new BufferedWriter(new FileWriter(employers));
            bw.write("Employer ID, Company Name, Company Street Address, Company City, Company State, Company Zip Code, Company Phone Number, Company e-mail address, Company Contact Person,");
            bw.close();
            files += "CSVFiles\\Employers.txt \n";
        }
        if(!fieldplacements.exists())
        {
            BufferedWriter bw = new BufferedWriter(new FileWriter(fieldplacements));
            bw.write("EmployeeID, Employer ID,");
            bw.close();
            files += "CSVFiles\\Evaluations.txt \n";
        }
        if(!evaluations.exists())
        {
            BufferedWriter bw = new BufferedWriter(new FileWriter(evaluations));
            bw.write("Evaluation Number, Employee Number, Employer Number, Evaluation Date, Next Evaluation Date, Work Quality Score, Work Quality Comments, Work Habits Score, Work Habits Comments, Job Knowledge Score, Job Knowledge Comments, Behavior Score, Behavior Comments, Overall Progress Score, Overall Comments, Average Score, Employment Recommendation,");
            bw.close();
            files += "CSVFiles\\FPs.txt \n";
        }   
        if(!files.equals(""))
        {
            JOptionPane.showMessageDialog(null,
                    "System created files: \n" + files);
        }
    }
    
    
