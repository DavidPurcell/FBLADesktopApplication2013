    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        //Lets Employer Login to their profile
        try {
            Empr = new Employer(jTextField1.getText());
            if(Empr.getData(0)==null)
            {
                jTextField1.setText("Not Found");
            }
            else if(Empr.getData(0).equals(jTextField1.getText()))
            {
                jTextField1.setText("Welcome");
                jPanel1.setVisible(true);
                jLabel3.setText("Switch Employer");
                jLabel1.setText(Empr.getData(1));
                setTitle("AHS FBLA Desktop Application - " + Empr.getData(1) + " Menu"); 
            }
            else
            {
                jTextField1.setText("Not Found");
            }
        } catch (IOException ex) {
            Logger.getLogger(Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

